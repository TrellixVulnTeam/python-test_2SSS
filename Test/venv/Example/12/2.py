#小结

#图形用户界面(GUI),GUI可以让程序更友好,虽然并不是所有的程序都需要它们,但是当程序要和用户交互时,GUI可能会有所帮助

#Python的GUI平台,Python程序猿有很多GUI平台，尽管有这么多选择是好事，但是选择时有时会很困难.

#wxPython
#wxPython 是成熟的并且特色丰富的跨平台Python GUI工具包

#布局
#通过指定几何坐标，可以直接将组件放置在想要的位置,但是，为了在包含它们的窗口改变大小时能做出适当的改变，
#需要使用布局管理器，wxPython 中的布局机制是尺寸器..

#事件处理
#用户的动作触发的GUI工具包的事件.任何应用中，程序都会有对于这些事件的反应,
#否则用户就没法和程序交互了,wxPython中事件处理函数使用Bind方法添加到组件上.

