#符号，对齐和用0填充
#在字段宽度和精度值之前还可以放置一个"标志",该标志可以是零,加号，减号或者空格
from math import pi
formatTest = '%010.2f' % pi
print(formatTest)
#使用减号(-)用来左对齐数值
formatTest1 = '%-10.2f' % pi
print(formatTest1)
#空白("")意味着在正数前加上空格,可以用来啊对齐正负数
print(('% 5d' % 10) + '\n' + ('% 5d' %  -10))
#加号(+),表示不管是正数还是负数都标识出符号(同样是用来对齐...)
print(('%+5d' % 10) + '\n' + ('%+5d' % -10))

